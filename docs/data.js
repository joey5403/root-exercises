// 获取所有分类
function getCategories() {
  return [...new Set(radicals.map(r => r.category))];
}

// 按分类筛选字根
function getRadicalsByCategory(category) {
  if (category === 'all') {
    return radicals;
  }
  return radicals.filter(r => r.category === category);
}

// 根据键位获取字根
function getRadicalsByKey(key) {
  return radicals.filter(r => r.key === key.toLowerCase());
}
